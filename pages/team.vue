<template>
    <div>
      <!-- About Section -->
      <section class="about">
          <div class="container">
              <h1 class="about__title">The forLoop Team</h1>
          </div>
      </section>

      <!-- Teams Section -->
      <section class="teams">
          <div class="team__list">
              <!-- Teams List Item -->
              <div v-for="(member, index) in members" :key="index" class="team__list__item position-relative" :style="{ backgroundImage: 'url(\''+member.profile_image+'\')'}">
                  <div class="team__list__item__container">
                      <div class="team__list__item__name">{{member.name}}</div>
                      <div class="team__list__item__handle">
                          <div class="handle__icon">
                              <svg class="icon-social-twitter icon-sm"><use xlink:href="img/icons.svg#icon-social-twitter"></use></svg>
                          </div>
                          <div class="handle__text">@{{member.twitter_handle}}</div>
                      </div>
                  </div>
              </div>
          </div>
      </section>


      <add-subscriber></add-subscriber>
    </div>
</template>


<script>
export default {
  name: 'TeamPage',
  data() {
    return {
      members: []
    };
  },
  asyncData({ app }) {
    return app.$axios
      .$get(`/team`)
      .then(res => {
        return { members: res.data };
      })
      .catch(e => {
        console.log('error', e.config);
        return {};
      });
  }
};
</script>

